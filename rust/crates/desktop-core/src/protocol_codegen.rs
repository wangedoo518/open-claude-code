//! Protocol type contract generator.
//!
//! Emits a TypeScript declaration file describing the JSON shape of
//! key `desktop-core` types so the frontend can import a single
//! source of truth instead of hand-maintaining matching `interface`
//! declarations in `apps/desktop-shell/src/lib/tauri.ts`.
//!
//! ── Approach ────────────────────────────────────────────────────
//!
//! Rather than introducing a heavy proc-macro dependency like `ts-rs`
//! or `schemars`, we build sample instances of each type, serialize
//! them with serde, and walk the resulting JSON value to emit a
//! corresponding TypeScript interface. This is a *reflection-based*
//! approach: the output mirrors whatever serde produces, so it's
//! always in sync with the actual wire format.
//!
//! Limitations:
//! - Optional fields are inferred from `null` in the sample (the
//!   sample must use `None`/`null` for fields that should be `?:`).
//! - Enum tags use the `#[serde(tag, rename_all)]` set on each enum.
//! - Nested objects are inlined; we don't deduplicate shared types.
//!
//! Generated file: `apps/desktop-shell/src/lib/protocol.generated.ts`
//!
//! Run via:
//!   cargo test -p desktop-core protocol_codegen -- --ignored --nocapture

use serde_json::Value;
use std::fmt::Write as _;

/// Walk a JSON value and emit a TypeScript type expression.
pub fn json_to_ts(value: &Value, indent: usize) -> String {
    match value {
        Value::Null => "null".to_string(),
        Value::Bool(_) => "boolean".to_string(),
        Value::Number(_) => "number".to_string(),
        Value::String(_) => "string".to_string(),
        Value::Array(arr) => {
            if arr.is_empty() {
                "unknown[]".to_string()
            } else {
                format!("Array<{}>", json_to_ts(&arr[0], indent))
            }
        }
        Value::Object(map) => {
            let pad = "  ".repeat(indent + 1);
            let close = "  ".repeat(indent);
            let mut out = String::from("{\n");
            for (k, v) in map {
                let _ = write!(
                    out,
                    "{pad}{}: {};\n",
                    quote_key(k),
                    json_to_ts(v, indent + 1)
                );
            }
            out.push_str(&close);
            out.push('}');
            out
        }
    }
}

fn quote_key(key: &str) -> String {
    let needs_quote = key.chars().any(|c| !c.is_alphanumeric() && c != '_');
    if needs_quote {
        format!("\"{key}\"")
    } else {
        key.to_string()
    }
}

/// Build a TypeScript declaration block for a single named type.
pub fn ts_interface(name: &str, sample: &Value) -> String {
    let body = json_to_ts(sample, 0);
    format!("export type {name} = {body};\n")
}

/// Build a TypeScript file from (name, sample) pairs.
pub fn ts_file(types: &[(&str, Value)]) -> String {
    let mut out = String::from(
        "// AUTO-GENERATED by desktop-core::protocol_codegen — do not edit by hand.\n\
         // Run `cargo test -p desktop-core protocol_codegen -- --ignored --nocapture`\n\
         // to regenerate from the Rust types.\n\
         //\n\
         // The shapes below are produced by serializing sample instances of\n\
         // each Rust type and walking the resulting JSON. They may be slightly\n\
         // looser than the Rust definitions (e.g. number vs u64) but reflect\n\
         // exactly what the HTTP API emits over the wire.\n\n",
    );
    for (name, sample) in types {
        out.push_str(&ts_interface(name, sample));
        out.push('\n');
    }
    out
}

